namespace PentaGE.Common
{
    // These values comes from Glfw.MouseButton and will be directly converted
    
    /// <summary>
    /// Represents mouse buttons.
    /// </summary>
    public enum MouseButton
    {
        /// <summary>
        /// The first mouse button.
        /// </summary>
        Button1 = 0,

        /// <summary>
        /// The second mouse button.
        /// </summary>
        Button2 = 1,

        /// <summary>
        /// The third mouse button.
        /// </summary>
        Button3 = 2,

        /// <summary>
        /// The fourth mouse button.
        /// </summary>
        Button4 = 3,

        /// <summary>
        /// The fifth mouse button.
        /// </summary>
        Button5 = 4,

        /// <summary>
        /// The sixth mouse button.
        /// </summary>
        Button6 = 5,

        /// <summary>
        /// The seventh mouse button.
        /// </summary>
        Button7 = 6,

        /// <summary>
        /// The eighth mouse button.
        /// </summary>
        Button8 = 7,

        /// <summary>
        /// Represents the left mouse button. Equivalent to <see cref="Button1"/>.
        /// </summary>
        Left = Button1,

        /// <summary>
        /// Represents the right mouse button. Equivalent to <see cref="Button2"/>.
        /// </summary>
        Right = Button2,

        /// <summary>
        /// Represents the middle mouse button. Equivalent to <see cref="Button3"/>.
        /// </summary>
        Middle = Button3
    }
}